public class Encapsulation { // get and set

    private String name; //
    private int age;




    // get the value
    public String getName(){
        return name;
    }
    

    // set the value
    public void setName(String name){
        this.name = name;
    }




    public int getAge(){
        return age;
    }



    public void setAge(int age){
        this.age = age;
    }
}
